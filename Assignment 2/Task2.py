#program to print the sum of number b/w 1 to 50 using for loop



# Initialize sum variable
sum = 0

# Loop from 1 to 50 
for i in range(1, 51):
    sum += i

# Print the result
print("The sum of numbers from 1 to 50 is: ", sum)
